public class ExoSwitch_1 {
    public static void main(String[] args) {
        int jour = 2;

        switch (jour) {
            case 1:
                System.out.println("Lundi");
                break;
            case 2:
                System.out.println("Mardi");
                break;
            case 3:
                System.out.println("Mercredi");
                break;
            default:
                System.out.println("Jour inconnu");
                break;
        }
    }
}
